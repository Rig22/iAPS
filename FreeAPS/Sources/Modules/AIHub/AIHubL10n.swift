/// Eigenes, leichtgewichtiges Lokalisierungssystem für den AI Hub.
///
/// Bewusst NICHT über Localizable.strings/Crowdin: Der AI Hub wird nicht in
/// den offiziellen iAPS-Source aufgenommen, also würde er dort nie übersetzt.
/// Stattdessen: Dictionary pro Sprache direkt im Code, UI-Sprache folgt der
/// App-Sprache (Bundle.main.preferredLocalizations), Fallback Englisch.
/// Abgedeckt werden die Sprachen der iAPS-Localizations.
enum AIHubL10n {
    /// UI-Sprachcode: bevorzugte App-Lokalisierung, normalisiert auf die
    /// Tabellen-Keys ("pt-BR" bleibt erhalten, sonst Basis-Subtag).
    static var languageCode: String {
        let preferred = Bundle.main.preferredLocalizations.first ?? "en"
        if tables[preferred] != nil { return preferred }
        let base = String(preferred.prefix(2))
        return tables[base] != nil ? base : "en"
    }

    /// Sprache der KI-Antworten: FoodSearch-Setting (geteilt) → App-Sprache.
    static var aiAnswerLanguageCode: String {
        UserDefaults.standard.userPreferredLanguageForAI ?? languageCode
    }

    /// Englischer Sprachname für Prompts ("German", "Dutch", …).
    static var aiAnswerLanguageName: String {
        Locale(identifier: "en").localizedString(forLanguageCode: aiAnswerLanguageCode)
            ?? "English"
    }

    static func t(_ key: String) -> String {
        tables[languageCode]?[key] ?? tables["en"]?[key] ?? key
    }

    static func t(_ key: String, _ args: CVarArg...) -> String {
        String(format: t(key), arguments: args)
    }

