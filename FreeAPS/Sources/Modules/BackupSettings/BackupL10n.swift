/// Eigenes, leichtgewichtiges Lokalisierungssystem für das Backup-Modul.
///
/// Gleiches Muster wie `AIHubL10n`: Bewusst NICHT über Localizable.strings/Crowdin,
/// weil das Backup-Modul nicht in den offiziellen iAPS-Source aufgenommen wird und
/// dort nie übersetzt würde. Kurze offizielle Keys ("Status", "Manual", "Automatic",
/// "Cancel", "OK") werden weiterhin implizit über SwiftUI/Localizable.strings genutzt.
enum BackupL10n {
    static var languageCode: String {
        let preferred = Bundle.main.preferredLocalizations.first ?? "en"
        if tables[preferred] != nil { return preferred }
        let base = String(preferred.prefix(2))
        return tables[base] != nil ? base : "en"
    }

    static func t(_ key: String) -> String {
        tables[languageCode]?[key] ?? tables["en"]?[key] ?? key
    }

    static func t(_ key: String, _ args: CVarArg...) -> String {
        String(format: t(key), arguments: args)
    }

